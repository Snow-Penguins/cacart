// Email regex pattern
export const emailRegexPattern = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
// This regex pattern checks for a valid email address format:
// - ^[^\s@]+: Starts with one or more characters that are not whitespace or @
// - @[^\s@]+: Followed by @ and one or more characters that are not whitespace or @
// - \.[^\s@]+$: Ends with a period (.) followed by one or more characters that are not whitespace or @
